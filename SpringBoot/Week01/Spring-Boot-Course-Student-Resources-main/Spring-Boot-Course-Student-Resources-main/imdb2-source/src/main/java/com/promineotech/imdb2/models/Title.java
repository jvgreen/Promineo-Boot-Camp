package com.promineotech.imdb2.models;

public class Title {
  private String id;
  private int type;
  private String name;
  
  public String getId() {
    return id;
  }
  public void setId(String id) {
    this.id = id;
  }
  
  public int getType() {
    return type;
  }
  public void setType(int type) {
    this.type = type;
  }
  
  public String getName() {
    return name;
  }
  public void setName(String name) {
    this.name = name;
  }
  
}
